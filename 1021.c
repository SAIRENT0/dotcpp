/*
//C语言程序设计教程（第三版）课后习题6.11
//题目描述
用迭代法求 平方根
公式：求a的平方根的迭代公式为： X[n+1]=(X[n]+a/X[n])/2 要求前后两次求出的差的绝对值少于0.00001。 输出保留3位小数
//输入
X
//输出
X的平方根
//样例输入
4
//样例输出
2.000
*/

#include <stdio.h>

int main(void)
{
	float a, x1, x2 = 1;

	scanf("%f", &a);

	do
	{
		x1 = x2;
		x2 = x1/2 + a/x1/2;
	}while((x1 > x2 ? x1-x2 : x2-x1) >= 0.00001);
	
	printf("%.3f\n", x2);

	return 0;
}
